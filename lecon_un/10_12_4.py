# четвертое упражнение
# площадь и периметр прямоугольника
print('\nчетвертое упражнение')

a = float(input('Введите длину прямоугольника: '))
# тип данных float, что бы расширить возможности для входных данных
b = float(input('Введите ширину прямоугольника: '))

square = a*b
perimetr = (a+b)*2

print('Площадь заданного прямоугольника равна:', square, 'кв. ед.')
print('Периметр заданного прямоугольника равен:', perimetr, 'ед.')
